package Loops;

public class Do_while_loop {
	
	
	public static void main(String[] args) {
		
		int r=129;
		do {System.out.println(r--);//updation
		}//incremental operator
		while(r>=128);
	// r=129/ 129print
	// r=128/ r>=128/ true 128print
	// r=127/ r>=128/ false null
		
	}

}
