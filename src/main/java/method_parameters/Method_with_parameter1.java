package method_parameters;

public class Method_with_parameter1 {
	
public static void main(String[] args) {
	
	maths(14,15.3,6,8.265);
	
}
public static void maths(int a, double b,int c, double d) {
	double j=a*b;
	double k=j/12.5;
	double l=k-c;
	double m=l+d;
	System.out.println(m);
	

}
}
